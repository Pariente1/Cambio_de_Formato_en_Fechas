def transformar_fecha(fecha_str):
  """
  Función para transformar una fecha en formato MMDDAAAA a AA-MM-DD, agregando ceros a la izquierda si es necesario.

  Parámetros:
    fecha_str (str): La fecha en formato MMDDAAAA.

  Retorno:
    str: La fecha en formato AA-MM-DD.
  """
  if not isinstance(fecha_str, str):
    raise ValueError("La fecha debe ser una cadena")

  fecha_str = fecha_str.zfill(8)

  dia = fecha_str[2:4]
  mes = fecha_str[:2]
  año = fecha_str[4:]

  return f"{año}-{mes}-{dia}"

